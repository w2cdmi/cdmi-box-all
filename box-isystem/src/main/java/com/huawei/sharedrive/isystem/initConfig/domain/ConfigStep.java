package com.huawei.sharedrive.isystem.initConfig.domain;

public class ConfigStep {

	private String enterpriseConfig;
	private String storageConfig;
	private String accessConfig;
	private String mailConfig;

	public String getEnterpriseConfig() {
		return enterpriseConfig;
	}

	public void setEnterpriseConfig(String enterpriseConfig) {
		this.enterpriseConfig = enterpriseConfig;
	}

	public String getStorageConfig() {
		return storageConfig;
	}

	public void setStorageConfig(String storageConfig) {
		this.storageConfig = storageConfig;
	}

	public String getAccessConfig() {
		return accessConfig;
	}

	public void setAccessConfig(String accessConfig) {
		this.accessConfig = accessConfig;
	}

	public String getMailConfig() {
		return mailConfig;
	}

	public void setMailConfig(String mailConfig) {
		this.mailConfig = mailConfig;
	}

}
